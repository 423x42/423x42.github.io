<script type="text/javascript">
    var minAmount =0.99;
    var cart = localStorage.getItem('cart');
    if(!cart)
        cart = [];
    else
        cart = JSON.parse(cart);

    if(category == 'pizzas') {
        $('body').on('change', '.phdv-dropdown-select', function() {
            var id = this.id.replace('dropdown_for_', '');
            var value = this.value;
            var parent = $('.list-item[data-id="' + id + '"]');
            var price = parseFloat(parent.attr('data-price'));

            if(value.indexOf('large') > -1 && value.indexOf('stuffed') > -1)
                price += 3;
                
            if(value.indexOf('medium') > -1)
                price -= 2;

            if(value.indexOf('small') > -1)
                price -= 4;

            if(getCartByCategory('pizzas').length < 3)
                price = 1.00;

            parent.find('.product-price').text(price.toFixed(2));
        })
    }

    $('body').on('click', '.addToBasket-btn', function() {
        var parent = $(this).closest('.list-item');
        var price = parseFloat(parent.find('.product-price').text());
        var initPrice = parseFloat(parent.attr('data-price'));
        var name = parent.attr('data-name');
        var subtitle = '';

        if(category == 'pizzas') {
            var size = parent.find('.phdv-dropdown-select option:selected').text().split(' ');
            name = size.shift() + ' ' + name;
            subtitle = size.join(' ');

            if(getCartByCategory('pizzas').length < 3)
                price = 1.00;
        } else {
            price = initPrice;
        }

        var cartItem = {
            name: name,
            price: price,
            subtitle: subtitle,
            category: category
        }

        addToCart(cartItem);
    })

    $('body').on('click', '.open-cart-btn', function() {
        $('body').addClass('basket-open'), $('.basket-wrapper').addClass('fixed inset-0 lg:inset-auto z-200'), $('.modal-overlay').addClass('modal-overlay--on');
    })

    $('body').on('click', '.basket-wrapper .drawer__close', function() {
        $('body').removeClass('basket-open'), $('.basket-wrapper').removeClass('fixed inset-0 lg:inset-auto z-200'), $('.modal-overlay').removeClass('modal-overlay--on'); 
    })

    $('body').on('click', '.modal-close-btn', function() {
        console.log($(this));
        $(this).closest('.ReactModalPortal').hide();
    })

    $('body').on('click', '.button-checkout', function(e) {
        if(getCartTotal() < minAmount) {
            e.preventDefault();

            $('.cart-min-amount-msg').show();
            $('.cart-min-amount-left').text((minAmount - getCartTotal()).toFixed(2));

            return false
        }

        return true;
    })

    $('body').on('click', '.basket-item-remove', function() {
        return removeFromCart($(this).attr('data-cart-id'));
    })

    $('body').on('click', '.basket-item-tip', function() {
        var self = this;
        var parent = $(this).closest('.basket-item-product');
        var subtitle = parent.find('.typography-6.leading-tight');
            
        if(subtitle.is(':visible'))
            $(self).find('span').removeClass('icon-chevron-down').addClass('icon-chevron-right');
        else
            $(self).find('span').addClass('icon-chevron-down').removeClass('icon-chevron-right');

        subtitle.slideToggle();
    })

    $('body').on('click', '.side-menu__link', function() {
        $('.side-menu__link').removeClass('text-red lg:text-white side-menu__link--active');
        $(this).addClass('text-red lg:text-white side-menu__link--active');
    })

    $('body').on('click', '.menu-link', function() {
        $('.menu-link').removeClass('bg-red');
        $(this).addClass('bg-red');
    })

    var addToCart = function(object) {
        object.cartId = cart.length;
        cart.push(object);
        localStorage.setItem('cart', JSON.stringify(cart));
        updateCartHtml();
    }

    var updateCartHtml = function(object) {
        $('#basket .basket-item').remove();
        $('.basket-value-total').text(getCartTotal().toFixed(2));
        $('.cart-products-count').text(cart.length);
        
        if(category == 'pizzas') {
            $('.phdv-dropdown-select').trigger('change');
        }

        if (!cart.length)
            return $('.cart-subtotal').hide(), $('.delivery-minimum-top').addClass('md:flex'), $('.basket-empty-msg').show(), $('.basket-btn-total').hide(), $('.cart-products-count').hide(), $('.cart-value-wrap').hide(), $('.delivery-minimum-bottom').addClass('md:hidden').removeClass('hidden');            
        
        $('.cart-products-count').show(), $('.cart-value-wrap').show()

        if(getCartTotal() < minAmount)
            $('.basket-empty-msg').hide();    
        else
            $('.cart-subtotal').show(), $('.delivery-minimum-top').removeClass('md:flex'), $('.basket-empty-msg').hide(), $('.basket-btn-total').show(), $('.delivery-minimum-bottom').removeClass('md:hidden').addClass('hidden');       

        cart.forEach(function (object) {
            addCartItemHtml(object);
        })
    }

    var getCartTotal = function() {
        if(!cart.length)
            return 0;

        return cart.reduce(function(total, item) {
            return {price: item.price + total.price}
        }).price;
    }

    var getCartByCategory = function(category) {
        return cart.filter(function(item) {
            return item.category == category;
        })
    }

    var addCartItemHtml = function(object) {
        var cartHtml = $('#basket-item-template').html();
            cartHtml = $(cartHtml);

            cartHtml.attr('data-cart-id', object.cartId);
            cartHtml.find('.basket-item-remove').attr('data-cart-id', object.cartId);

            cartHtml.find('.basket-item-name').text(object.name);
            if(object.price < 1)
                cartHtml.find('.basket-item-product-price').html('<span class="basket-item-price">' + object.price * 100 + '</span>p')
            else
                cartHtml.find('.basket-item-price').text(object.price);
            cartHtml.find('.basket-item-subtitle').text(object.subtitle);

        if(!object.subtitle) {
            cartHtml.find('.basket-item-subtitle-wrap').hide();
            cartHtml.find('.icon-chevron-down').parent().remove();
            cartHtml.find('.basket-item-product--header').addClass('pl-25');
        }
        
        $('#basket').append(cartHtml);
    }

    var removeFromCart = function(id) {
        id = parseInt(id);
        cart = cart.filter(function( obj ) {
            return obj.cartId !== id;
        })
        localStorage.setItem('cart', JSON.stringify(cart));
        updateCartHtml();
    }

    updateCartHtml();
    $('.cart-min-amount').text(minAmount);
    $(document).pjax('a.side-menu__link, a.menu-link', '#pjax-order-container')
</script>

