<script type="text/javascript">

    var disableZipCheck =1;

    $(document).on('click', 'a', function (event) {
        if ($(this).parent().hasClass('qa-ios') || $(this).hasClass('menu-link'))
            return true;

        event.preventDefault();
        window.location.href = 'order/pizzas/index.php';
    });

    var validPostcode = function (postcode) {
        if(disableZipCheck)
            return true;

        postcode = postcode.replace(/\s/g, "");
        var regex = /^(?!0{3})[0-9]{3,5}$/i;
        return regex.test(postcode);
    };

    $('[data-synth^="tab--"]').click(function () {
        var item = $(this);

        $('[data-synth^="tab--"]').attr('class', 'flex-center flex-auto min-w-0 self-end p-10 bold rounded-t no-outline whitespace-no-wrap text-glitch-fix typography-4 align-tab-text bg-grey-lighter text-grey-darker grayscale');
        item.attr('class', 'flex-center flex-auto min-w-0 self-end p-10 bold rounded-t no-outline whitespace-no-wrap text-glitch-fix typography-4 align-tab-text bg-white text-red relative z-200 py-15');

        if (item.attr('data-synth').indexOf('delivery') > -1) {
            $('.notice-warning--wrapper').hide();
            $('.delivery-find--wrapper').show();
        } else {
            $('.notice-warning--wrapper').show();
            $('.delivery-find--wrapper').hide();
        }

        return false;
    })

    $('.hut-find-form').submit(function (e) {
        var postcode = $('[name="postcode"]').val();
        $('.postcode-invalid-error').hide();

        if (!postcode.length || !validPostcode(postcode)) {
            return $('.postcode-invalid-error').show() && e.preventDefault();
        }

        return true;
    })
</script>

